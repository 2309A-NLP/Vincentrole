# ============================================================
# 前后端数据拆分函数
# ============================================================


def frontend_role_config():
    """
    为前端 UI 提供角色配置

    特点：
    - 使用深拷贝，避免修改原始数据
    - 移除 system_prompt，防止前端拿到敏感 Prompt
    """
    frontend_data = deepcopy(ROLE_CATALOG)
    for role in frontend_data.values():
        role.pop("system_prompt", None)
    return frontend_data


def backend_character_prompts():
    """
    为后端 LLM / RAG 提供 system prompt 映射

    返回格式：
    {
        "doctor_tcm": "...",
        "friend_anime": "...",
        ...
    }
    """
    return {
        role_id: role["system_prompt"]
        for role_id, role in ROLE_CATALOG.items()
    }